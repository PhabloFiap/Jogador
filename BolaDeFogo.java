
public class BolaDeFogo {

	private int poder;
	private int x;
	private int y;
	
	
	
	public BolaDeFogo(int poder, int x, int y) {
		super();
		this.poder = poder;
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}



}
